    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        modelUsuario = new ModelUsuario();
        modelUsuario.setUsuNome(jtfNomeUsuario.getText());
        modelUsuario.setUsuLogin(jtfLogin.getText());
        modelUsuario.setUsuSenha(String.valueOf(jpfSenha.getPassword()));
        if (cadAlt.equals("cad")) {
            if (controllerUsuario.salvarUsuarioController(modelUsuario)) {
            //Cadastrou usuario
            JOptionPane.showMessageDialog(this, "Usuario cadastrado com sucesso !!!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            //limpar dados digitados
            limparFormulario();
            carregarUsuarios();
        } else {
            //não cadastrou usuario
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        }else{   
             modelUsuario.setUsuId(Integer.parseInt(jtfCodigo.getText()));
             if (controllerUsuario.atualizarUsuarioController(modelUsuario)) {
            //Alterou usuario
            JOptionPane.showMessageDialog(this, "Usuario alterado com sucesso !!!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            //limpar dados digitados
            limparFormulario();
            carregarUsuarios();
        } else {
            //não alterou usuario
            JOptionPane.showMessageDialog(this, "Erro ao alterar usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
       
    }//GEN-LAST:event_jbSalvarActionPerformed

