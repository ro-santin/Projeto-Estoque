    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jtTabela.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Favor selecionar usuário a ser excluido");
        } else {
            int codigo = (int) jtTabela.getValueAt(linha, 0);
            if (controllerUsuario.excluirUsuarioController(codigo)) {
                carregarUsuarios();
                limparFormulario();
                JOptionPane.showMessageDialog(this, "Usuario excluido com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir usuario");
            }
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // pegar o id para buscar no db:
        cadAlt = "alt";
        modelUsuario = new ModelUsuario();
        int linha = jtTabela.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Favor selecionar usuário a ser excluido");
        } else {
            int codigo = (int) jtTabela.getValueAt(linha, 0);
           modelUsuario = controllerUsuario.getUsuarioController(codigo); 
           jtfCodigo.setText(String.valueOf(modelUsuario.getUsuId()));
           jtfLogin.setText(modelUsuario.getUsuLogin());
           jpfSenha.setText(modelUsuario.getUsuSenha());
           jtfNomeUsuario.setText(modelUsuario.getUsuNome());
           
        }           
    }//GEN-LAST:event_jbAlterarActionPerformed
    /**
     * Limpa dados digitados no formulário após clicar
     */
    private void limparFormulario() {
        jtfCodigo.setText("");
        jtfNomeUsuario.setText("");
        jtfLogin.setText("");
        jpfSenha.setText("");
        cadAlt = "cad";
    }

    /**
     * Carrega uma lista de usuarios NA tabela
     */
    private void carregarUsuarios() {
        listaUsuario = controllerUsuario.getListaUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) jtTabela.getModel();
        modelo.setNumRows(0); //Limpa a tabela

        for (int i = 0; i < listaUsuario.size(); i++) {
            modelo.addRow(new Object[]{
                listaUsuario.get(i).getUsuId(),
                listaUsuario.get(i).getUsuNome(),
                listaUsuario.get(i).getUsuLogin()
            });
        }

    }

